/**
 * @ClassName: EurekaApplication
 * @Description: 微服务注册中心
 * @author Somnus
 * @date 2018年9月25日
 */
@EnableEurekaServer
@SpringBootApplication
public class EurekaApplication {

	public static void main(String[] args) {
		SpringApplication.run(EurekaApplication.class, args);
	}
}
